     */
    public function store(Request $request)
    {
        Post::create([
            'title' => $request->input('title'),
            'description' => $request->input('description'),
            'tag' => $request->input('tag'),
            'img_path' => 'not added'
        ]);

        return redirect('/')->with('success', 'Your blog has been posted. Please browse through');
    }

